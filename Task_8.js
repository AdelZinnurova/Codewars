// Very simple, given a number (integer / decimal / both depending on the language), find its opposite (additive inverse).

// Очень просто: задано число (целое / десятичное / оба в зависимости от языка), найдите его противоположность (аддитивную обратную величину).


// Examples:

// 1: -1
// 14: -14
// -34: 34



function opposite(number) {
    return -number;
  }